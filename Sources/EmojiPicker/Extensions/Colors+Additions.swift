import SwiftUI

extension Color {
    
    /**
     * The color used for the drag indicator and other secondary surfaces.
     */
    static let secondarySurface = Color("Secondary Surface", bundle: .module)
    
    /**
     * The color used for the category selection circle, as well as other tertiary surfaces.
     */
    static let tertiarySurface = Color("Tertiary Surface", bundle: .module)
    
    /**
     * The color used for the category section title.
     */
    static let sectionTitle = Color("Section Title", bundle: .module)
}
